package android.accessguarded.guarded365.co.uk.guardedguard.login;

/**
 * Immutable model class for a User.
 */
public class User {

    private String Username;
    private int UserId;

    public User() {
    }

    public String getUsername() {
        return Username;
    }

    public int getId() {
        return UserId;
    }
}
